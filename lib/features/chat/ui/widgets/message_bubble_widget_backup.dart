        setState(() {
          _isPlayingThisMessage = false;
        });
      } else {
        // ‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡πÄ‡∏•‡πà‡∏ô ‡πÉ‡∏´‡πâ‡πÄ‡∏£‡∏¥‡πà‡∏°‡πÄ‡∏•‡πà‡∏ô
        setState(() {
          _isPlayingThisMessage = true;
        });

        // ‡πÄ‡∏•‡πà‡∏ô‡πÄ‡∏™‡∏µ‡∏¢‡∏á‡∏à‡∏≤‡∏Å base64
        final bool success = await _audioPlayer.playFromBase64(
          widget.message.text,
        );

        if (!success) {
          // ‡πÄ‡∏•‡πà‡∏ô‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
          setState(() {
            _isPlayingThisMessage = false;
          });

          if (mounted) {
            ScaffoldMessenger.of(context).showSnackBar(
              SnackBar(
                content: Text('‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏•‡πà‡∏ô‡πÄ‡∏™‡∏µ‡∏¢‡∏á‡πÑ‡∏î‡πâ'),
                backgroundColor: Colors.red,
              ),
            );
          }
        } else {
          // ‡πÄ‡∏•‡πà‡∏ô‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à ‡∏£‡∏≠‡πÉ‡∏´‡πâ‡∏à‡∏ö‡πÅ‡∏•‡πâ‡∏ß‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞
          // ‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å 2-3 ‡∏ß‡∏¥‡∏ô‡∏≤‡∏ó‡∏µ‡∏à‡∏∞‡∏´‡∏¢‡∏∏‡∏î‡πÄ‡∏≠‡∏á (‡∏´‡∏£‡∏∑‡∏≠‡πÉ‡∏ä‡πâ callback)
          Future.delayed(const Duration(seconds: 15), () {
            if (mounted) {
              setState(() {
                _isPlayingThisMessage = false;
              });
            }
          });
        }
      }
    } catch (e) {
      setState(() {
        _isPlayingThisMessage = false;
      });

      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÄ‡∏•‡πà‡∏ô‡πÄ‡∏™‡∏µ‡∏¢‡∏á: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

